/**
 * Define current state of game
 * MENU - The title screen
 * HOWTOPLAY - How to play the game, accessed from Menu
 * RULES - How to play the game, accessed when playing game
 * DIFFICULTY - Select difficulty of game
 * HIGHSCORE - Displays top 10 scores
 * INGAME - The actual game play
 * FINISH - When the player has completed the current game
 * @author mahima z5113392
 */
public enum STATE {
	MENU, HOWTOPLAY, DIFFICULTY,INGAME, WIN, SELECTION;
}
